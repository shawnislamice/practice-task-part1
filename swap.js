let a = 5;
let b = 7;
console.log(
  `---------------------Before Swapping----------------------- \nFirst Number is: ${a}\nSecond Number is: ${b}`
);
let temp = a;
a = b;
b = temp;
console.log(
  `-------------------------After Swapping---------------------------- \nFirst Number is: ${a}\nSecond Number is: ${b}`
);
